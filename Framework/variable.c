//======================================================================
//获得CRC16值 
//puchMsg:要校验的数组 
//usDataLen:数组长度 
//======================================================================
u16 Get_rtuCrc16(u8 *puchMsg,u16 usDataLen) 
{ 
    u8 uchCRCHi=0xFF; 		//高CRC 字节初始化 
    u8 uchCRCLo=0xFF; 		//低CRC 字节初始化  
    u32 uIndex; 	 		//CRC 循环中的索引 
	while(usDataLen--) 	//传输消息缓冲区 
	{ 
		uIndex=uchCRCHi^*puchMsg++; //计算CRC  
		uchCRCHi=uchCRCLo^auchCRC16_Hi[uIndex]; 
		uchCRCLo=auchCRC16_Lo[uIndex]; 
	} 
	return (uchCRCHi<<8|uchCRCLo); 
}

u8 Check_XOR(u8 *data,u8 lenth)//异或校验
{
    u8 i,temp;
    temp=0;
    for(i=0;i<lenth;i++)
    {
        temp ^= data[i];
    }
    return temp;
}

unsigned char AscToHex(unsigned char aChar)//ASCII转hex，正常输出范围0x00-0x0F，输入范围为0x30-0x39,0x41-0x46，0x61-0x66
{
    unsigned char ret;
    if((aChar >= 0x30)&&(aChar <= 0x39))
    {
        ret = aChar - 0x30;
    }
    else if((aChar >= 0x41)&&(aChar <= 0x46))
    {
        ret = aChar - 0x37;
    }
    else if((aChar >= 0x61)&&(aChar <= 0x66))
    {
       ret = aChar - 0x57;
    }
    else
    {        
       ret = 0xFF;
    }
    return ret;
}

